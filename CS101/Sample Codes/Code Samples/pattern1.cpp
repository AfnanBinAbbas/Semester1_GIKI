# include <iostream>
using namespace std;
int main()
{
	for(int i=1; i<=14; i++)
	{
		for(int j=1; j<=14-i; j++)
		{
			cout<<"*";
		}
		cout<<endl;
	}
	
	
	cout<<"************************************************************"<<endl;
	cout<<"************************************************************"<<endl;
	cout<<"************************************************************"<<endl;
	cout<<"************************************************************"<<endl;
	cout<<"************************************************************"<<endl;
	cout<<endl<<endl;
	cout<<"USING WHILE LOOP:"<<endl<<endl;
	
	int i=1;
	while(i<=14)
	{
		i++;
		
		int j=1;
		while(j<=14-i)
		{
			cout<<"*";
			j++;
		}
		cout<<endl;
	}
} 
