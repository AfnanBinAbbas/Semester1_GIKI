# include <iostream>
using namespace std;
int main()
{
	//RECTANGLE PATTERN
	
	int rows,columns;
	
	cout<<"Enter no: of rows:  ";
	cin>>rows;
	
	cout<<endl<<"Enter no: of columns: ";
	cin>>columns;
	
	for(int i=1; i<=rows; i++)
	{
		for(int j=1; j<=columns; j++)
		{
		
		cout<<"*";
	}
	
	cout<<endl;
}
           


}
